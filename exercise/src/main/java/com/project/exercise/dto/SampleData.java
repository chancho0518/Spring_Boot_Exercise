package com.project.exercise.dto;

public class SampleData {

    private int id;
    private  String name;

    public SampleData() {
    }

    public SampleData(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
